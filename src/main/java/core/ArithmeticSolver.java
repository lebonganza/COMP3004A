package core;

public class ArithmeticSolver {
	
	public double add(double leftO,double rightO) {
		return leftO+rightO;
	}
	
	public double substract(double leftO,double rightO) {
		return leftO-rightO;
	}
	
	public double multiply(double leftO,double rightO) {
		return leftO*rightO;
	}
	
	public double divide(double leftO,double rightO) {
		return leftO/rightO;
	}
	
	public double modulus(double leftO,double rightO) {
		return  leftO%rightO;
	}
}
